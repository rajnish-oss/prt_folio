const useMobileDetection = () => {
  const [isMobile, setIsMobile] = useState(false);
  const [isLowEnd, setIsLowEnd] = useState(false);
  const [preferReducedMotion, setPreferReducedMotion] = useState(false);

  useEffect(() => {
    const checkDevice = () => {
      const { isMobile, isLowEnd, preferReducedMotion } = detectDeviceCapabilities();
      setIsMobile(isMobile);
      setIsLowEnd(isLowEnd);
      setPreferReducedMotion(preferReducedMotion);
    };

    checkDevice();
    const resizeListener = throttle(checkDevice, 500);
    window.addEventListener('resize', resizeListener);
    window.matchMedia('(prefers-reduced-motion: reduce)').addEventListener('change', checkDevice);

    return () => {
      window.removeEventListener('resize', resizeListener);
      window.matchMedia('(prefers-reduced-motion: reduce)').removeEventListener('change', checkDevice);
    };
  }, []);

  return { isMobile, isLowEnd, preferReducedMotion };
};

